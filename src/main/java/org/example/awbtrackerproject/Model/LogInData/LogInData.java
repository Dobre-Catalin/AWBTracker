package org.example.awbtrackerproject.Model.LogInData;

public class LogInData {
    String email;
    String password;

    public LogInData(String email, String password) {
        this.email = email;
        this.password = password;
    }

    public String getEmail() {
        return email;
    }

    public String getPassword() {
        return password;
    }


}
